void solve()
{
    int n, m, x;
    std::cin >> n >> m;
    std::unordered_map<int, int> mp;
    std::vector<int> a(n), c;
    for (int i = 0; i < n; i++)
    {
        std::cin >> x;
        mp[x] = 1;
        c.push_back(x);
    }
    for (int i = 0; i < m; i++)
    {
        std::cin >> x;
        c.push_back(x);
    }
    sort(begin(c), end(c));
    bool res = false;
    for (int i = 1; i < m + n; i++)
    {
        if (mp.find(c[i]) != mp.end() && mp.find(c[i - 1]) != mp.end())
        {
            res = true;
            break;
        }
    }
    std::cout << (res ? "Yes" : "No") << "\n";
}

int32_t main()
{
    std::ios_base::sync_with_stdio(false);
    std::cin.tie(nullptr);
    // std::cout<<std::fixed<<std::setprecision(0);

    int t = 1;
    // std::cin >> t;
    while (t--)
        solve();
}
