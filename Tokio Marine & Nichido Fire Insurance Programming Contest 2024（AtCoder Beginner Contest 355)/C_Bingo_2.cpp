void solve()
{
    int n, t, z, res = 0;
    std::cin >> n >> t;
    std::unordered_map<int, std::pair<int, int>> m;
    for (int i = 1; i <= n; i++)
    {
        for (int j = 1; j <= n; j++)
        {
            int sc = n * (i - 1) + j;
            m[sc] = {i, j};
        }
    }
    std::vector<int> rows(n + 1), cols(n + 1);
    int d1 = 0, d2 = 0;
    while (t--)
    {
        res++;
        std::cin >> z;
        auto p = m[z];
        int x = p.first, y = p.second;
        rows[x]++;
        cols[y]++;
        if (x == y)
            d1++;
        if (x + y == n + 1)
            d2++;
        if (rows[x] == n || cols[y] == n || d1 == n || d2 == n)
        {
            std::cout << res << " ";
            return;
        }
    }
    std::cout << "-1\n";
}

int32_t main()
{
    std::ios_base::sync_with_stdio(false);
    std::cin.tie(nullptr);
    // std::cout<<std::fixed<<std::setprecision(0);

    int t = 1;
    // std::cin >> t;
    while (t--)
        solve();
}